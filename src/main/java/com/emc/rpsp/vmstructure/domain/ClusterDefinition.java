package com.emc.rpsp.vmstructure.domain;

public class ClusterDefinition {

	private String id;
	private String name;

	public ClusterDefinition(String id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
