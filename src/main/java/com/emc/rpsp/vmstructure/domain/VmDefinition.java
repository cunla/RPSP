package com.emc.rpsp.vmstructure.domain;

public class VmDefinition {

    private String id;
    private String name;

    public VmDefinition(String id, String name) {
        super();
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
